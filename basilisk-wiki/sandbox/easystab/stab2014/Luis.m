# Contributions of Luis Bernardos

## [Backwards Facing Step](./BackwardsFacingStep.m)
Steady Navier-Stokes incompressible simulation of a Backwards Facing Step, analysis of the recurciulating zones at varying Reynolds.

![Recirculation](./BackwardsFacingStep_Rec.png)

## [Cavity Lid Driven](./CavityLidDriven.m)
Steady Navier-Stokes incompressible simulation of a Cavity Lid Driven.

![Cavity Lid Driven](./CavityLidDrive_SL.png)

## [Poisson 2D Hollow Disc](../poisson_2D_hollow_disc.m)
Testing of a Multi-Domain 2D poisson function.

![Poisson Multi-domain](../poisson_2D_hollow_disc.png)

## [Unsteady Multi-Domain Poiseuille Flow](./PoiseuilleUnsteady.m)
Testing the Multi-Domain 2D for the Unsteady Navier-Stokes equations. Analysis of the establishing length.

![Simulation Poiseuille](./Poiseuille_Re1000.gif)

## [Jet Stream Flow through a Pipe](./JetUnsteady.m)
Simulation of the instabilities that occur in a jet stream through a pipe. Unsteady Navier-Stokes incompressible simulation.

![Jet stream simulation](http://www.acro3d.com/public/joomla/images/Jet_Vor_Re6000.gif)

## [Flow around a Cylinder](./CylinderFlow.m)
Simulation of a flow around a cylinder, Navier-Stokes Unsteady incompressible. An attempt to simulate Von-Karman street.

![Flow around a cylinder](http://www.acro3d.com/public/joomla/images/cylinder_re6000.gif)

## [Taylor-Couette Flow](./TaylorCouette.m)
The famous Taylor-Couette Flow, solving the Steady Navier-Stokes Equations in cartesian form. Comparison with the theoretical solution.

![Taylor-Couette Flow pressure field and velocity vector field](./TaylorCouette.png)

## [Steady Navier Stokes](./NavierStokesStationnary)
Works on the Navier Stokes Steady equations.

## [Steady Cylindric Navier Stokes](./NavierStokesStationnaryCylindric)
Works on the Navier Stokes steady equations in cylindrical form.

## [Mesh manipulation](./Lspacing.m)
A very useful function for shrinking the mesh near one or two walls by means of interpolating equations.

## [Brusselator 2D Eigenmode analysis and Simulation](./brusselator2D_eigenmodes.m)
A slightly different version than [Guillaume's brusselator](./brusselator_2D_periodic.m), that we make an eigenmode analysis and a rather cool simulation gif:

![Brusselator 2D Simulation](http://www.acro3d.com/public/joomla/images/bruss2D_simul.gif)

## [UNFINISHED Roughness flow](./Roughness.m)
