## Convergence analysis of the Poisson equation in 2D
Through this analysis [poisson2D_convergence.m](./poisson2D_convergence.m) we study the convergence of the Poisson equation in 2D, and we compare the results with finite difference methods. The reader may be interested also in consulting [poisson2D.m](../poisson2D.m) and Natasha's & Charline's analysis [diffmat2D_test_functions.](./diffmat2D_test_functions.m).





